PRAGMA foreign_keys=OFF;
BEGIN TRANSACTION;
CREATE TABLE customers (id INTEGER PRIMARY KEY AUTOINCREMENT,name TEXT NOT NULL,email TEXT NOT NULL,phone TEXT);
CREATE TABLE products (id INTEGER PRIMARY KEY AUTOINCREMENT,name TEXT NOT NULL,price REAL NOT NULL,description TEXT);
CREATE TABLE invoices (id INTEGER PRIMARY KEY AUTOINCREMENT,customer_id INTEGER NOT NULL,product_id INTEGER NOT NULL,quantity INTEGER NOT NULL,total_price REAL NOT NULL,date TEXT NOT NULL,FOREIGN KEY (customer_id) REFERENCES customers(id),FOREIGN KEY (product_id) REFERENCES products(id));
DELETE FROM sqlite_sequence;
COMMIT;
